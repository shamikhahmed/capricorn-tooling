#!/usr/bin/env node
/**
 * sync-design-system.mjs
 * Concatenates the shared design-system core layers and vendors the
 * result into each app as css/capricorn-core.css (plus motion runtime JS).
 *
 * Source of truth: shared/design-system/ — never edit the copies.
 */
import { readFileSync, writeFileSync, existsSync, mkdirSync, copyFileSync, readdirSync } from 'node:fs';
import { join } from 'node:path';
import { WORKSPACE_ROOT as ROOT, SCRIPTS_DIR, SHARED_DIR } from './lib/workspace-root.mjs';
const DS = join(SHARED_DIR, 'design-system');

const CORE_LAYERS = ['tokens.css', 'a11y.css', 'motion.css', 'premium.css', 'motion-premium.css'];
const COMPONENT_LAYER = 'components.css';
const JS_FILES = [
  'capricorn-motion.js',
  'capricorn-scene.js',
  'capricorn-premium-nav.js',
  'capricorn-cinematic.js',
  'capricorn-deck.js',
  'capricorn-deck-pro.js',
  'capricorn-pitch.js',
];
const VENDOR_FILES = ['gsap.min.js', 'ScrollTrigger.min.js'];

// dest: app dir; components: include the marketing component layer
const TARGETS = [
  { app: 'shamikhahmed.github.io', cssDir: 'css', jsDir: 'js', components: true },
  { app: 'VaultCap', cssDir: 'css', jsDir: 'js', components: false },
  { app: 'PulseCap', cssDir: 'css', jsDir: 'js', components: false },
  { app: 'PrismCap', cssDir: 'css', jsDir: 'js', components: false },
  { app: 'SteadyCap', cssDir: 'css', jsDir: 'js', components: false },
  { app: 'LedgerCap', cssDir: 'css', jsDir: 'js', components: false },
  { app: 'DeePonyCap', cssDir: 'css', jsDir: 'js', components: false },
  { app: 'ScentCap', cssDir: 'public/css', jsDir: 'public/js', components: false },
  { app: 'AuraCap', cssDir: 'public/css', jsDir: 'public/js', components: false },
];

const banner = (files) =>
  `/* GENERATED by scripts/sync-design-system.mjs — DO NOT EDIT.\n` +
  `   Source: shared/design-system/{${files.join(',')}} */\n\n`;

const layer = (f) => readFileSync(join(DS, f), 'utf8');

function syncVendor(appDir, jsDir) {
  const vendorSrc = join(DS, 'vendor');
  const vendorDest = join(appDir, jsDir, 'vendor');
  if (!existsSync(vendorSrc)) return;
  mkdirSync(vendorDest, { recursive: true });
  for (const f of VENDOR_FILES) {
    const src = join(vendorSrc, f);
    if (existsSync(src)) copyFileSync(src, join(vendorDest, f));
  }
}

let synced = 0;
for (const t of TARGETS) {
  const appDir = join(ROOT, t.app);
  if (!existsSync(appDir)) { console.warn(`skip ${t.app} (missing)`); continue; }

  const files = t.components ? [...CORE_LAYERS, COMPONENT_LAYER] : CORE_LAYERS;
  const css = banner(files) + files.map(layer).join('\n');
  const cssDir = join(appDir, t.cssDir);
  mkdirSync(cssDir, { recursive: true });
  writeFileSync(join(cssDir, 'capricorn-core.css'), css);

  const jsDirPath = join(appDir, t.jsDir);
  mkdirSync(jsDirPath, { recursive: true });

  for (const jsFile of JS_FILES) {
    const js = banner([jsFile]) + layer(jsFile);
    writeFileSync(join(jsDirPath, jsFile), js);
  }

  syncVendor(appDir, t.jsDir);

  synced++;
  console.log(`✓ ${t.app} → ${t.cssDir}/capricorn-core.css + ${JS_FILES.length} JS + vendor`);
}
console.log(`design-system synced to ${synced} app(s)`);
